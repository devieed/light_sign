// 使用 SEO 钩子函数
export function useSEO(language = 'en') {
  const seo = seoTranslations[language] || seoTranslations['en']
  
  // 更新页面标题
  const updateTitle = () => {
    document.title = seo.title
  }
  
  // 更新 meta 标签
  const updateMeta = (name, content) => {
    let element = document.querySelector(`meta[name="${name}"]`)
    if (!element) {
      element = document.createElement('meta')
      element.setAttribute('name', name)
      document.head.appendChild(element)
    }
    element.setAttribute('content', content)
  }
  
  const updateProperty = (property, content) => {
    let element = document.querySelector(`meta[property="${property}"]`)
    if (!element) {
      element = document.createElement('meta')
      element.setAttribute('property', property)
      document.head.appendChild(element)
    }
    element.setAttribute('content', content)
  }
  
  // 应用 SEO
  const applySEO = () => {
    updateTitle()
    updateMeta('description', seo.description)
    updateMeta('keywords', seo.keywords)
    
    // Open Graph
    updateProperty('og:title', seo.ogTitle)
    updateProperty('og:description', seo.ogDescription)
    
    // Twitter
    updateProperty('twitter:title', seo.ogTitle)
    updateProperty('twitter:description', seo.ogDescription)
    
    // 更新 HTML lang 属性
    document.documentElement.lang = language
  }
  
  return {
    seo,
    applySEO,
    updateTitle,
    updateMeta,
    updateProperty
  }
}

